    public void showCategoryProducts () {
        productList.removeAll();
        if (filtrar.getSelectedIndex()== 0){
            for (int j = 0; j < farmacia.catalogo.getCategories().get(0).getProducts().size(); j++) {
                productList.add(farmacia.catalogo.getCategories().get(0).getProducts().get(j));
            }
        }
        if (filtrar.getSelectedIndex()== 1){
            for (int j = 0; j < farmacia.catalogo.getCategories().get(1).getProducts().size(); j++) {
                productList.add(farmacia.catalogo.getCategories().get(1).getProducts().get(j));
            }
        }
        if (filtrar.getSelectedIndex()== 2){
            for (int j = 0; j < farmacia.catalogo.getCategories().get(2).getProducts().size(); j++) {
                productList.add(farmacia.catalogo.getCategories().get(2).getProducts().get(j));
            }
        }
        if (filtrar.getSelectedIndex()== 3){
            for (int j = 0; j < farmacia.catalogo.getCategories().get(3).getProducts().size(); j++) {
                productList.add(farmacia.catalogo.getCategories().get(3).getProducts().get(j));
            }
        }
        if (filtrar.getSelectedIndex()== 4){
            for (int j = 0; j < farmacia.catalogo.getCategories().get(4).getProducts().size(); j++) {
                productList.add(farmacia.catalogo.getCategories().get(4).getProducts().get(j));
            }
        }
        if (filtrar.getSelectedIndex()== 5){
            for (int j = 0; j < farmacia.catalogo.getCategories().get(5).getProducts().size(); j++) {
                productList.add(farmacia.catalogo.getCategories().get(5).getProducts().get(j));
            }
        } 
    }
    
    public void showAll () {
        productList.removeAll();
        for (int i = 0; i < farmacia.catalogo.products.size(); i++) {
            productList.add(farmacia.catalogo.products.get(i).getName());
        }
    }
    
    public void showShoppingCart () {
        shoppingCartList.removeAll();
        for (int i = 0; i < farmacia.carro.prods.size(); i++) {
            shoppingCartList.add(farmacia.carro.prods.get(i).getName());
        }
    }
    public void showProductDetails (Product selected) {
        productDetails.removeAll();
        productDetails.add("Nombre: " + selected.getName());
        productDetails.add("Código: " + selected.getCode());
        productDetails.add("Prospecto: " + selected.getProspect());
        productDetails.add(selected.priceS() + " euros");
    }
    
    public float showPrice (Product selected) {
        float precio = selected.getPrice();
        return precio;
    }
    
