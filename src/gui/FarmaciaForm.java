    private void addToCartActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addToCartActionPerformed
        // TODO add your handling code here:
        int active;
        int active2;
        active = productList.getSelectedIndex();
        active2 = filtrar.getSelectedIndex();
        Product selected;
        selected = farmacia.catalogo.categories.get(active2).prod.get(active);
        farmacia.carro.addProduct(selected);
        float prec = 0;
        for (int i = 0; i < farmacia.carro.prods.size(); i++) {
            prec += farmacia.carro.prods.get(i).getPrice();
            String precStr = Float.toString(prec);
            precioTotal.setText(precStr);
        }
        showShoppingCart();
    }//GEN-LAST:event_addToCartActionPerformed

    private void removeProductActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removeProductActionPerformed
        // TODO add your handling code here:
        int active;
        active = shoppingCartList.getSelectedIndex();
        Product selected;
        selected = farmacia.carro.prods.get(active);
        farmacia.carro.deleteProduct(selected);
        showShoppingCart();
        String prec = precioTotal.getText();
        float precioFl = Float.parseFloat(prec);
        String precStr = "";
        for (int i = 0; i < farmacia.carro.prods.size(); i++) {
            precioFl -= farmacia.carro.prods.get(i).getPrice();
            precStr = Float.toString(precioFl);
            precioTotal.setText(precStr);
        }
        showShoppingCart();
    }//GEN-LAST:event_removeProductActionPerformed

