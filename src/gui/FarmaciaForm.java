    private void recetaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_recetaActionPerformed
        // TODO add your handling code here:
        int active;
        int active2;
        active = productList.getSelectedIndex();
        active2 = filtrar.getSelectedIndex();
        Product selected;
        selected = farmacia.catalogo.categories.get(active2).prod.get(active);
        farmacia.carro.addProduct(selected);
        float precioNormal = selected.getPrice();
        float dis = 0.3f;
        float descuento = precioNormal * dis;
        float precioDescontado = precioNormal - descuento;
        selected.setPrice(precioDescontado);
        String precioDescontadoStr = Float.toString(precioDescontado);
        
        if (!"".equals(recetaE.getText())) {
        farmacia.carro.addProduct(selected);
        shoppingCartList.add(selected.getName());
        //String precioStr = Float.toString(showPrice(selected));
        farmacia.carro.addProduct(selected);
        precioTotal.setText(precioDescontadoStr);
        }
    }//GEN-LAST:event_recetaActionPerformed

